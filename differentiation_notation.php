    <h1>Differentiation Notation</h1>
    <p>When given a variable <b>y</b> expressed as a function of another variable <b>x</b> such that <b>y = f(x)</b>,
    we can differentiate <b>y</b> with respect to <b>x</b>. In terms of mathematical notation, the
    derivative of the general function <b>y = f(x)</b> can be expressed as:</p>
    <p>$$ \frac{dy}{dx} \ \text{or} \ \frac{d}{dx} y \ \text{or} \ f'(x) \ \text{or} \ \frac{d}{dx} f(x) \ \text{or} \ y' $$</p>
    <p>It is important to note that  \( \frac{dy}{dx} \) does not represent a fraction such that you can cancel out terms in the numerator or denominator.</p>
