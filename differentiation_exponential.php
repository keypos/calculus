    <p>
    An exponential function possesses a value that is raised to the power that contains the variable of interest. When the base value is the constant "e", there are special rules which exist for differentiating exponential functions.<br><br>
    Where \( y = e^{f(x)} \)
    </p>
    <p>
        \[ \frac{dy}{dx} = f'(x)e^{f(x)} \]
    </p>
    <p>
        That is, the derivative of the exponent becomes the new coefficient of “e”, and the original expression with base “e” remains the same.<br><br>
        For example,
    </p>
    <p>
        \[ y=2e^{4x^2} \]
        \[ \frac{dy}{dx}=2(8x)e^{4x^2} \]
        \[ \frac{dy}{dx}=16xe^{4x^2} \]
    </p>
