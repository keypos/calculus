    <p>There are two types of integration on a function:</p>
   <p>
        The <b>indefinite integral</b> of \(f(x)\) with respect to \(x\), written as
        \[\int f(x) \, dx\]
        The <b>definite integral</b> of \(f(x)\) between limits \(a\) and \(b\) with respect to \(x\), written as
        \[\int_a^b f(x) \, dx\]
    </p>
    <p>Lets break down what this means. First define <b>d</b> as a little bit of.</p>
    <b>d “a little bit of”</b>
    <p>We can represent an infinitesimally small change in <b>x</b> with <b>dx</b>. Thus <b>dx</b> means a little bit of <b>x</b> and <b>du</b> means a little bit of <b>u</b>.<br>
    Now we can introduce a new symbol <b>\( \int \)</b> (a squiggly s). This simply means the sum of.</p>
    <b>\( \int \) “the sum of”</b>
    <p>Thus <b>\( \int \)dx</b> means the sum of all the little bits of <b>x</b>. Or <b>\( \int \)dt</b> means the sum of all the little bits of <b>t</b>. This is called the integral. If <b>x</b> is considered as made up of a lot of little bits, each of which is called <b>dx</b>, if you add them all up together you get the sum of all the <b>dx's</b> (which is the same thing as the whole of <b>x</b>).</p>
    <b>\( \int \) dx "the sum of little bits of x"</b>
