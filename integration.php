    Now we can introduce a new symbol <b>∫</b> (a squiggly s). This simply means the sum of.</p>
    <b>∫ “the sum of”</b>
    <p>Thus <b>∫dx</b> means the sum of all the little bits of <b>x</b>. Or <b>∫dt</b> means the sum of all the little bits of <b>t</b>. This is called the intergral. If <b>x</b> is considered as made up of a lot of little bits, each of which is called <b>dx</b>, if you add them all up together you get the sum of all the <b>dx's</b> (which is the same thing as the whole of <b>x</b>).</p>
    <b>∫ dx "the sum of little bits of x"</b>
    <p>The word “integral” simply means “the whole.” <br>
    Think of an hour cut up into 3600 little bits called seconds. The whole of the 3600 little bits added up together makes one hour.</p>
