    <p>Differentiation is a branch of calculus that involves finding the rate of change of one variable with respect to another variable. In practice, this commonly involves finding the rate of change of a curve.</p>
    <p>You differentiate a function in order to find its derivative, which is a measure of how that function (the output)
    changes as its input changes.</p>
    <p>A useful concept in differentiation is the tangent which is a straight line that just touches a curve at any given point. The slope of a tangent at any point is the measure of the rate of change of a curve at that particular point.</p>
